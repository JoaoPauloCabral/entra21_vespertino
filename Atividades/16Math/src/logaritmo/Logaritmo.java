package logaritmo;

public class Logaritmo {

	public static void main(String[] args) {

				//Variavel nr contendo um numero
				double nr = 5;
				
				//Trabalhando com as funcoes log
				System.out.println("Resultado: " +Math.log(nr+1));
				
				System.out.println("Resultado: " +Math.log10(nr+1));
				
				System.out.println("Resultado: " +Math.log1p(nr+1));
		
	}

}
