package potencia;

public class PotenciaRaiz {

	public static void main(String[] args) {

				//Potencia
				System.out.println(Math.pow(5, 2));
				
				//Raiz quadrada
				System.out.println(Math.sqrt(25));
				
				//Raiz cubica
				System.out.println(Math.cbrt(27));
				
				//Constante de Euler
				System.out.println(Math.exp(2));
		
	}

}
