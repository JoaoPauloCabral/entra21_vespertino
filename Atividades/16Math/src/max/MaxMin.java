package max;

public class MaxMin {

	public static void main(String[] args) {

		//Array de numeros
		double numeros[] = {1,15};
		
		//Exibir o maior e o menor numero
		System.out.println("O maior numero e " +Math.max(numeros[0], numeros[1]));
		System.out.println("O menor numero e " +Math.min(numeros[0], numeros[1]));
		
	}

}
