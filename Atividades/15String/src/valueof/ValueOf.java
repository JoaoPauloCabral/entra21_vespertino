package valueof;

public class ValueOf {

	public static void main(String[] args) {

		//Criando uma variavel do tipo String
		String quantidade = "";
		
		//Criando uma variavel do tipo int e atribuido o valor 5
		int numero = 5;
		
		//Atribuindo o valor da variavel int na variavel String
		quantidade  = String.valueOf(numero);
	
	}

}
