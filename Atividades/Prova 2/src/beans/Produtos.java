package beans;

public class Produtos {

	private boolean batata, feijao, arroz, macarrao, cenoura, maca, queijo, presunto;

	public boolean isBatata() {
		return batata;
	}

	public void setBatata(boolean batata) {
		this.batata = batata;
	}

	public boolean isFeijao() {
		return feijao;
	}

	public void setFeijao(boolean feijao) {
		this.feijao = feijao;
	}

	public boolean isArroz() {
		return arroz;
	}

	public void setArroz(boolean arroz) {
		this.arroz = arroz;
	}

	public boolean isMacarrao() {
		return macarrao;
	}

	public void setMacarrao(boolean macarrao) {
		this.macarrao = macarrao;
	}

	public boolean isCenoura() {
		return cenoura;
	}

	public void setCenoura(boolean cenoura) {
		this.cenoura = cenoura;
	}

	public boolean isMaca() {
		return maca;
	}

	public void setMaca(boolean maca) {
		this.maca = maca;
	}

	public boolean isQueijo() {
		return queijo;
	}

	public void setQueijo(boolean queijo) {
		this.queijo = queijo;
	}

	public boolean isPresunto() {
		return presunto;
	}

	public void setPresunto(boolean presunto) {
		this.presunto = presunto;
	}
	
}
